# Jaka jest roznica pomiedzy while a for?

# Przyklad uzycia while (Nieokreslona liczba iteracji)
print("While loop:")
i = 0
while i < 5: # Petla bedzie wykonywana dopoki i < 5
    print(i)
    i += 1
# Wynik: 0 1 2 3 4

print()

# Przyklad uzycia for (Okreslona liczba iteracji)
print("For loop:")
for i in range(5): # Petla bedzie wykonywana dla i = 0, 1, 2, 3, 4
    print(i)
# Wynik: 0 1 2 3 4