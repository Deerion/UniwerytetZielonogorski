function y = zwieksz(x)
    % ZWIEKSZ Zwieksza swoj argument o jeden.
    % Funkcja ZWIEKSZ(x) wykonuje dzialanie ZWIEKSZ(x) -> x + 1.
    % 
    % Przyklad:
    %     ZWIEKSZ(2) = 3
    %
    % Wejście:
    %     x - liczba lub macierz liczbowa
    %
    % Wyjście:
    %     y - wartość x powiększona o 1

    y = x + 1;
end
